ORG 90 ; DIGITS FOR LEFT HAND
	
	DB FA
	DB A
	DB B6
	DB 9E
	DB 4E
	DB DC
	DB 7C
	DB 8A
	DB FE
	DB CE

ORG A0 ; DIGITS FOR RIGHT HAND
	
	DB FB
	DB B
	DB B7
	DB 9F
	DB 4F
	DB DD
	DB 7D
	DB 8B
	DB FF
	DB CF


ORG 00 ; STARTING CODE

MOV CL, 90 ; START OF TABLE FOR LEFT HAND DIGITS
MOV DL, A0 ; START OF TABLE FOR RIGHT HAND DIGITS

MOV BL, 0A ; COUNTDOWN FOR LEFT HAND DIGITS

JMP lefthand ; SET FIRST LEFTHAND NUMBER AS 0

start:


	MOV DL, A0 ; SET DL AS START OF TABLE

	
righthand:
		

	MOV AL, [DL] ; MOVE RIGHT HAND DIGIT INTO AL
	OUT 02 ; OUTPUT IT ON 7 SEGMENT DISPLAY	

	INC DL ; INCREMENT DL
	
	CMP DL, AA ; IF AT END OF TABLE MOVE TO LEFTHAND, ELSE JUMP BACK TO RIGHTHAND
	JZ lefthand
	JNZ righthand

lefthand:

	CMP BL, 00 ; IF BL == 0, FINISH, ELSE CONTINUE
	JZ finish

	MOV AL, [CL] ; MOVE LEFTHAND DIGIT INTO AL
	OUT 02 ; OUTPUT ONTO 7 SEGMENT DISPLAY
	
	DEC BL ; DECREMENT BL
	INC CL ; INCREMENT CL

	JMP start ; JUMP BACK TO START


finish:
	END