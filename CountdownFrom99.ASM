ORG 90 ; DIGITS FOR LEFT HAND
	
	DB 00
	DB FA
	DB A
	DB B6
	DB 9E
	DB 4E
	DB DC
	DB 7C
	DB 8A
	DB FE
	DB CE

ORG A0 ; DIGITS FOR RIGHT HAND
	
	DB 00
	DB FB
	DB B
	DB B7
	DB 9F
	DB 4F
	DB DD
	DB 7D
	DB 8B
	DB FF
	DB CF


ORG 00


MOV CL, AA ; END OF TABLE FOR RIGHT HAND DIGITS
MOV DL, 9A ; END OF TABLE FOR LEFT HAND DIGITS


MOV AL, [DL] ; CHANGE NUMBER OF LEFT HAND DIGIT		
OUT 02

CALL 50


ORG 50
START:	

	MOV AL, [CL] ; CHANGE NUMBER OF RIGHT HAND DIGIT
	OUT 02

	CMP AL, 00 ; IF AL == 0, FINISH, ELSE GO AGAIN
	JZ RESET	

	DEC CL ; GO THROUGH TABLE FOR RIGHT HAND DIGITS
	

	;DEC DL ; GO THROUGH TABLE FOR LEFT HAND DIGITS

	
	JMP START

RESET:
	CMP DL, 91 ; IF DL == 0, END PROGRAM
	JZ FINISH

	MOV CL, AA;
	
	DEC DL ; GO THROUGH TABLE FOR LEFT HAND DIGITS

	MOV AL, [DL] ; CHANGE NUMBER OF LEFT HAND DIGIT		
	OUT 02

	CALL 50

FINISH:
	END

